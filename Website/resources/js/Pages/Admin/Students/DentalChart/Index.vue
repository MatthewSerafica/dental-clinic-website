<script setup>
import { useForm } from '@inertiajs/vue3'

const emit = defineEmits(['openNewRecordModal'])

const props = defineProps({
    chart: Object,
})

const form = useForm({
    teeth: props.chart.map(tooth => ({
        id: tooth.chart_id,
        number: tooth.tooth_number,
        condition1: tooth.condition_one,
        condition2: tooth.condition_two,
        classObject: {
            topClass: `${tooth.mesial}`,
            leftClass: `${tooth.buccal}`,
            bottomClass: `${tooth.distal}`,
            rightClass: `${tooth.lingual}`,
            centerClass: `${tooth.occlusal}`,
        },
    })),
})

const changeColor = (id, position) => {
    id = id - 1;
    const colors = ['green', 'blue', 'red'];
    const currentColor = form.teeth[id].classObject[position];
    const colorIndex = (colors.indexOf(currentColor) + 1) % colors.length;
    form.teeth[id].classObject[position] = `${colors[colorIndex]}`;
}

const chartUpdate = () => {
    const chartsData = form.teeth.map(tooth => ({
        id: tooth.id,
        topColor: tooth.classObject.topClass,
        leftColor: tooth.classObject.leftClass,
        bottomColor: tooth.classObject.bottomClass,
        rightColor: tooth.classObject.rightClass,
        centerColor: tooth.classObject.centerClass,
        condition1: tooth.condition1,
        condition2: tooth.condition2,
    }))
    form.put(route('admin.chart.update'), { chartsData },  { preserveScroll: true, preserveState: false })
    emit('openNewRecordModal')
}
</script>

